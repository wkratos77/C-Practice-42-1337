#include <unistd.h>

int	main(int ac, char **ag)
{
	int	i;
	char	c;

	if (ac == 2)
	{
		i = 0;
		while (ag[1][i])
		{
			if (ag[1][i] != '_')
				write (1, &ag[1][i], 1);
			else
			{
				c = ag[1][i + 1] - 32;
				write (1, &c, 1);
				i++;
			}
			i++;
		}
	}
	write (1, "\n", 1);
}
